// write a function to check odd or even number
//you need to do it in 2 ways- has return and no return

function OddEvenCheck(number) {
    if (number % 2 == 0) {
        console.log("this is a even number");
    }
    else {
        console.log("this is a odd number")
    }
}
OddEvenCheck(5);
OddEvenCheck(10);
OddEvenCheck(4857);
OddEvenCheck(1465);
